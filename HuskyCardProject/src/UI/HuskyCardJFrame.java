    private void displayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayButtonActionPerformed
        String nuid = nuidText.getText();
        String firstName = firstNameText.getText();
        String lastName = lastNameText.getText();
        String collegeName = collegeNameText.getText();
        String phoneOff = phoneOffText.getText();
        String phonePer = phonePerText.getText();
        String emailOff = emailOffText.getText();
        String emailPer = emailPerText.getText();
        String street = streetText.getText();
        String apt = aptText.getText();
        String cityState = cityStateText.getText();
        String zipCode = zipCodeText.getText();
        this.person.setNuid(nuid);
        this.person.setFirstName(firstName);
        this.person.setLastName(lastName);
        this.person.setCollegeName(collegeName);
//        this.person.setPhoneOff(phoneOff);
//        this.person.setPhonePer(phonePer);
//        this.person.setEmailOff(emailOff);
//        this.person.setEmailPer(emailPer);
        
        Address addressOfPerson = this.person.getAddress();
        addressOfPerson.setStreet(street);
        addressOfPerson.setApt(apt);        
        addressOfPerson.setCityState(cityState);
        addressOfPerson.setZipCode(zipCode);
        
        Contact personalContact = this.person.getPersonalContact();
        personalContact.setEmail(emailPer);
        personalContact.setPhone(phonePer);
        
        Contact officeContact = this.person.getOfficeContact();
        officeContact.setEmail(emailOff);
        officeContact.setPhone(phoneOff);
        //JOptionPane.showMessageDialog(null, "SAVED SUCCESSFULLY!");
        
        display();
    }//GEN-LAST:event_displayButtonActionPerformed

