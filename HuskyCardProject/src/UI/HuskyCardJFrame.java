    private void display(){
        if(this.person.getNuid().isEmpty()
           || this.person.getFirstName().isEmpty()
           || this.person.getLastName().isEmpty()
           || this.person.getCollegeName().isEmpty()
           || this.person.getOfficeContact().getPhone().isEmpty()
           || this.person.getPersonalContact().getPhone().isEmpty()
           || this.person.getOfficeContact().getEmail().isEmpty()
           || this.person.getPersonalContact().getEmail().isEmpty()
           || this.person.getAddress().getStreet().isEmpty()
           || this.person.getAddress().getApt().isEmpty()
           || this.person.getAddress().getCityState().isEmpty()
           || this.person.getAddress().getZipCode().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Please enter all values!!");}
        else{
        nuidDisplay.setText(this.person.getNuid());
        firstNameDisplay.setText(this.person.getFirstName());
        lastNameDisplay.setText(this.person.getLastName());
        collegeNameDisplay.setText(this.person.getCollegeName());
        phoneOffDisplay.setText(this.person.getOfficeContact().getPhone());
        phonePerDisplay.setText(this.person.getPersonalContact().getPhone());
        emailOffDisplay.setText(this.person.getOfficeContact().getEmail());
        emailPerDisplay.setText(this.person.getPersonalContact().getEmail());
        streetDisplay.setText(this.person.getAddress().getStreet());
        aptDisplay.setText(this.person.getAddress().getApt());
        cityStateDisplay.setText(this.person.getAddress().getCityState());
        zipCodeDisplay.setText(this.person.getAddress().getZipCode());}
    }
    
