package Model;

/**
 *
 * @author pulkit
 */
public class Contact {
    String email;
    String phone;

    public Contact(){
        this.email = "";
        this.phone = "";
    }
    
    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
